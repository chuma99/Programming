#this first conversion will be celcius to kelvin
celsius = float(input("Please enter degrees in celsius: "))
kelvin = celsius + 273.15
print("The temperature in Kelvin:",kelvin)
#this first conversion will be celsius to fahrenheit
celsius = float(input("Please enter degrees in celsius: "))
fahrenheit = (1.8 * celsius) + 32
print("The temperature in Fahrenheit:",fahrenheit)